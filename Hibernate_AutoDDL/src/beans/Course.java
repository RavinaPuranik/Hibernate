package beans;

public class Course {

	 private int cid;
	 private String cname;
	 private int fee;
	 
	 public void setCid(int cid) {
		this.cid = cid;
	}
	 
	 public int getCid() {
		return cid;
	}
	 
	 public void setCname(String cname) {
		this.cname = cname;
	}
	 
	 public String getCname() {
		return cname;
	}
	 
	 public void setFee(int fee) {
		this.fee = fee;
	}
	 
	 public int getFee() {
		return fee;
	}
	
}
